public class PhepCong {
    public int tinhCong(int a, int b){
        return a + b;
    }
    public int tinhCong(int a, int b, int c){
        return a + b + c;
    }
    
    public double tinhCong(double a, double b){
        return a + b;
    }
    
    public double tinhCong(double a, double b, double c){
        return a + b + c;
    }
}
